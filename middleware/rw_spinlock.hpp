#ifndef RW_SPINLOCK_HPP
#define RW_SPINLOCK_HPP

#include "hamcast/util/atomic_operations.hpp"

namespace hamcast { namespace middleware {

// Implements SharedLockable concept:
// http://www.boost.org/doc/libs/1_41_0/doc/html/thread/synchronization.html
class rw_spinlock
{

    volatile boost::int32_t m_flag;

 public:

    rw_spinlock();

    void lock();
    void unlock();
    bool try_lock();

    void lock_shared();
    void unlock_shared();
    bool try_lock_shared();

};

} } // namespace hamcast::middleware

#endif // RW_SPINLOCK_HPP
